async function seedCompleteTranslations() {
  console.log('🌱 Seeding complete translations (English & Hindi)...');
  console.log(`Total translations to seed: ${translations.length}`);

  try {
    let successCount = 0;
    let errorCount = 0;

    for (const translation of translations) {
      try {
        await prisma.translation.upsert({
          where: {
            key_language_namespace: {
              key: translation.key,
              language: translation.language,
              namespace: translation.namespace,
            },
          },
          update: {
            value: translation.value,
          },
          create: translation,
        });
        successCount++;
        if (successCount % 50 === 0) {
          console.log(`✓ Processed ${successCount} translations...`);
        }
      } catch (error) {
        console.error(`✗ Error with ${translation.key} (${translation.language}):`, error.message);
        errorCount++;
      }
    }

    console.log(`\n✅ Successfully seeded ${successCount} translations!`);
    if (errorCount > 0) {
      console.log(`⚠️  ${errorCount} translations had errors`);
    }

    // Show summary
    const enCount = await prisma.translation.count({ where: { language: 'en' } });
    const hiCount = await prisma.translation.count({ where: { language: 'hi' } });
    console.log(`\n📊 Translation Summary:`);
    console.log(`   English: ${enCount} translations`);
    console.log(`   Hindi: ${hiCount} translations`);
    console.log(`   Total: ${enCount + hiCount} translations`);
  } catch (error) {
    console.error('❌ Error seeding translations:', error);
    throw error;
  } finally {
    await prisma.$disconnect();
  }
}

seedCompleteTranslations()
  .catch((error) => {
    console.error(error);
    process.exit(1);
  });

